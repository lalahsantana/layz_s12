def cm_para_metros(cm):
    """
    Converte centímetros para metros.
    :param cm: Comprimento em centímetros
    :return: Comprimento em metros
    """
    return cm / 100

def metros_para_cm(m):
    """
    Converte metros para centímetros.
    :param m: Comprimento em metros
    :return: Comprimento em centímetros
    """
    return m * 100
